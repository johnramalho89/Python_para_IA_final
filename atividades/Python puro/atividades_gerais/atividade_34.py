nome = input("Digite seu nome: ").strip()

if nome == "Huguinho" or nome == "Zezinho" or nome == "Luisinho":
    print("Você é um dos sobrinhos do Pato Donald!")

elif nome == "Chiquinho" or nome == "Francisquinho":
    print("Você é um dos sobrinhos do Mickey Mouse!")

else:
    print("Você não é sobrinho do Pato Donald nem do Mickey Mouse.")
